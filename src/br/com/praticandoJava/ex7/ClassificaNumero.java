package br.com.praticandoJava.ex7;

public class ClassificaNumero {
    public static void main(String[] args) {
        int numero = 7;

        if (numero % 2 == 0) {
            System.out.println("Numero é par");
        } else {
            System.out.println("Numero é impar");
        }
    }
}
